int main() {

	long a = 200000;
	long b = 120000;

	if( a > b ) {
		puts("200000 > 120000\n");

	} else {
		puts("200000 < 120000\n");
	}

	a = 1;
	b = 120000;

	if( a > b ) {
		puts("1 > 120000\n");

	} else {
		puts("1 < 120000\n");
	}

	a = 123456;
	b = 12345;

	if( a > b ) {
		puts("123456 > 12345\n");

	} else {
		puts("123456 < 12345\n");
	}
	return 0;


}
